#include <stdio.h>

int main() {
    printf("Single quote: \' \n");
    printf("Double quote: \" \n");
    printf("Question mark: \? \n");
    printf("Backslash: \\ \n");
    printf("Alert (beep): \a \n");
    printf("Backspace: X\bY \n");
    printf("Newline: Line1\nLine2\n");
    printf("Carriage return: Line1\rLine2\n");
    printf("Tab: Before\tAfter\n");

    return 0;
}
